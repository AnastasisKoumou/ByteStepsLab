    private Worksheet ws(String title, String theory, Question question) {
        return new Worksheet(title, theory, question);
    }

    private Question mc(String prompt, String correctAnswer, String explanation, String... options) {
        return new Question(QuestionType.MULTIPLE_CHOICE, prompt, List.of(options), List.of(correctAnswer), explanation);
    }

    private Question tf(String prompt, boolean correct, String explanation) {
        return new Question(QuestionType.TRUE_FALSE, prompt, List.of("Σωστό", "Λάθος"), List.of(String.valueOf(correct)), explanation);
    }

    private Question fill(String prompt, String explanation, String... correctAnswers) {
        return new Question(QuestionType.FILL_BLANK, prompt, List.of(), List.of(correctAnswers), explanation);
    }
